export const getParksLayerOptions = setClickpointInfo => ({
  style: {
    fillColor: '#f14600',
    fillOpacity: 0.1,
    color: '#f14600',
    strokeOpacity: 1,
    weight: 1,
    cursor: 'pointer',
  },
  onEachFeature: (feature, layer) => {
    layer.on('click', e => {
      DomEvent.stopPropagation(e)
      setClickpointInfo({
        location: { ...e.latlng },
        address: {
          type: 'adres',
          weergavenaam: feature.properties.name,
          id: '',
        },
      })
    })
  },
})

